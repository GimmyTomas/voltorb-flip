const BoardTypeParams& BoardTypeData::params(Level level, BoardTypeIndex type) {
    return paramsTable_[level - 1][type];
}

int64_t BoardTypeData::permutations(Level level, BoardTypeIndex type) {
    return permutationsTable_[level - 1][type];
}

int64_t BoardTypeData::acceptedCount(Level level, BoardTypeIndex type) {
    return acceptedTable_[level - 1][type];
}

int64_t BoardTypeData::permutationsGcd(Level level) {
    return permutationsGcdTable_[level - 1];
}

int64_t BoardTypeData::acceptedGcd(Level level) {
    return acceptedGcdTable_[level - 1];
}

int BoardTypeData::reducedPermutations(Level level, BoardTypeIndex type) {
    return reducedPermutationsTable_[level - 1][type];
}

int64_t BoardTypeData::reducedAccepted(Level level, BoardTypeIndex type) {
    return reducedAcceptedTable_[level - 1][type];
}

bool BoardTypeData::isCompatible(Level level, BoardTypeIndex type, int totalVoltorbs,
                                 int totalSum) {
    const auto& p = params(level, type);
    if (p.n0 != totalVoltorbs) return false;

    int expectedSum = p.n1() + 2 * p.n2 + 3 * p.n3;
    return expectedSum == totalSum;
}

